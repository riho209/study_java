package day3;

public class s305{
    public static void main(String[] args) {
        int num=1;
        switch (num) {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;

            default:
                System.out.println("それ以外の値");
                break;
        }
    }
}